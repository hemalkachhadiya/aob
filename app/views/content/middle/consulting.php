            </header>
            <section class="block-text-h2">
                <p><span class="bold-text">Мотивация</span> — на первом месте. Для обучения есть маркетологи, внутренние тренеры. </p>
                <ul class="list-dash">
                    <li>Мотивированы ли ваши сотрудники?</li>
                    <li>Зачем они приходят на работу?</li>
                    <li>Каковы доминирующие мотивы групп сотрудников?</li>
                    <li>Мотивация на результат или на процесс?</li>
                    <li>Какие мотивы можно использовать для дальнейшего развития и как использовать их правильно?</li>
                    <li>Как сделать так, чтобы сотрудник был исполнителем, но <span class="marked">менеджером</span>?</li>
                </ul>
                <p><a href="#">Узнать больше об истинных мотивах</a> и превратить отдел в команду единомышленников.</p>
                <p><span class="bold-text">Обученный персонал</span> — это не тот, кто знает наизусть технические характеристики товара, но тот, кто знает продающие моменты товара. Тот, кто уверен, что продает не только цена. Тот, кто уверен, что для каждой категории клиента существует своя система аргументации.</p>
                <p><a href="#">Выявите эти продающие моменты</a> и уйдите от шаблонов и единообразия презентаций.</p>
